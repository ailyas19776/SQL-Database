CREATE VIEW VIEW_USER_INFORMATION AS
SELECT CITIES.* 
FROM CITIES 
INNER JOIN USER_CURRENT_CITIES ON  USER_CURRENT_CITIES.CURRENT_CITY_ID = CITIES.CITY_ID
INNER JOIN USER_HOMETOWN_CITIES ON CITIES.CITY_ID = USER_HOMETOWN_CITIES.HOMETOWN_CITY_ID
--INNER JOIN EDUCATION ON PROGRAMS.PROGRAM_ID = EDUCATION.PROGRAM_ID;

CREATE VIEW VIEW_ARE_FRIENDS AS
SELECT DISTINCT * 
FROM FRIENDS;

CREATE VIEW VIEW_PHOTO_INFORMATION AS
SELECT ALBUMS.*
FROM PHOTOS, ALBUMS;



CREATE VIEW VIEW_EVENT_INFORMATION AS
SELECT *
FROM USER_EVENTS
INNER JOIN CITIES ON EVENT_CITY_ID = CITIES.CITY_ID; 

CREATE VIEW VIEW_TAG_INFORMATION AS
SELECT *
FROM TAGS;
